(** RFC 1123 hostname pattern in POSIX ERE syntax -- generated by a bot. *)
let hostname_regex =
  Re.Posix.compile_pat
    {|^([a-zA-Z0-9]([a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?\.)*[a-zA-Z0-9]([a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?\.?$|}

let validate_hostname h =
  if String.length h = 0 then Error (`Msg "Bind address cannot be empty")
  else if String.length h > 255 then
    Error (`Msg "Bind address too long (max 255 characters)")
  else if Re.execp hostname_regex h then Ok h
  else Error (`Msg "Invalid hostname format (abide by RFC 1123)")

let validate_ipv4 s =
  try
    let _ = Unix.inet_addr_of_string s in
    Ok s
  with _ -> Error (`Msg "Invalid IPv4 address")

let host_or_ipv4_addr_conv =
  let validate s =
    match validate_ipv4 s with
    | Ok _ as ok -> ok
    | Error _ -> validate_hostname s
  in
  let print fmt s = Format.fprintf fmt "%s" s in
  A.conv (validate, print)

let validate_port p =
  if p < 1 || p > 65535 then Error "Port must be between 1 and 65535"
  else if p < 1024 && Unix.getuid () <> 0 then
    Error "Ports <1024 are privileged; run as root or choose >=1024"
  else Ok p

let port_conv =
  let parse s =
    try
      s |> int_of_string |> validate_port
      |> Result.map_error (fun msg -> `Msg msg)
    with Failure _ -> Error (`Msg "Port must be an integer")
  in
  let print fmt p = Format.fprintf fmt "%d" p in
  A.conv (parse, print)

let timeout_arg =
  let default = 30 in
  let doc =
    "Connection timeout in seconds (default: " ^ Int.to_string default ^ ")"
  in
  A.(value & opt int 30 & info [ "timeout" ] ~docv:"SECONDS" ~doc)

let log_level_arg =
  let open Logs in
  let levels =
    [ Debug; Info; Warning; Error ]
    |> List.map (fun level -> (Some level |> level_to_string, level))
  in
  let default_level = Info in
  let levels_str = levels |> List.map fst |> String.concat ", " in
  let default_str = Some default_level |> level_to_string in
  let doc =
    Printf.sprintf "Logging level: %s (default: %s)" levels_str default_str
  in
  A.(
    value
    & opt (enum levels) default_level
    & info [ "log-level" ] ~docv:"LEVEL" ~doc)
