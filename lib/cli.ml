let port_server_arg =
  let default = 4242 in
  let doc =
    "Server port to listen on (default " ^ Int.to_string default ^ ")"
  in
  A.(value & opt port_conv default & info [ "port"; "p" ] ~docv:"PORT" ~doc)

let bind_arg =
  let default = "localhost" in
  let doc = "Bind address (IPv4 addr or hostname, default: " ^ default ^ ")" in
  A.(
    value
    & opt host_or_ipv4_addr_conv default
    & info [ "bind" ] ~docv:"ADDR" ~doc)

let port_client_arg =
  let default = 4242 in
  let doc =
    "Server port to connect to (default: " ^ Int.to_string default ^ ")"
  in
  A.(value & opt port_conv default & info [ "port"; "p" ] ~docv:"PORT" ~doc)

let host_arg =
  let default = "localhost" in
  let doc = "Server hostname or IP (default: " ^ default ^ ")" in
  A.(
    value
    & opt host_or_ipv4_addr_conv default
    & info [ "host" ] ~docv:"HOST" ~doc)

let run_server port bind timeout log_level =
  let terminal = Server.make_terminal_config ~log_level () in
  let net = Server.{ bind; timeout; port } in
  try Lwt_main.run (Server.run ~terminal ~net ())
  with e ->
    Lwt_io.eprintf "Fatal server error: %s\n%!" (Printexc.to_string e)
    |> Lwt_main.run;
    exit 1

let run_client host port timeout log_level =
  let terminal = Client.make_terminal_config ~log_level () in
  let net = Client.{ host; timeout; port } in
  try Lwt_main.run (Client.run ~terminal ~net ())
  with e ->
    Lwt_io.eprintf "Fatal client error: %s\n%!" (Printexc.to_string e)
    |> Lwt_main.run;
    exit 1

let cmd =
  let open Cmdliner in
  Cmd.group
    (Cmd.info "chat" ~version:"0.1.0" ~doc:"Simple one-on-one chat application")
    [
      Cmd.make
        (Cmd.info "server" ~doc:"Start a chat server")
        Term.(
          const run_server $ port_server_arg $ bind_arg $ timeout_arg
          $ log_level_arg);
      Cmd.make
        (Cmd.info "client" ~doc:"Connect to a chat server")
        Term.(
          const run_client $ host_arg $ port_client_arg $ timeout_arg
          $ log_level_arg);
    ]

let run () = exit (Cmdliner.Cmd.eval cmd)
